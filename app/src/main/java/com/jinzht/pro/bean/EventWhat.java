package com.jinzht.pro.bean;

/**
 * EventBus的数字标识
 */
public class EventWhat {
    private int what;

    public EventWhat() {
    }

    public EventWhat(int what) {
        this.what = what;
    }

    public int getWhat() {
        return what;
    }

    public void setWhat(int what) {
        this.what = what;
    }
}
